# ================================================================
# 1. 「钩子」的本体意象
# ================================================================
# 想象一个钓鱼钩：
#
#   鱼线 (系统流程)        ──────────────── 正常流程
#   鱼钩 (Hook 点)               ╭───╮
#                                │ 钩 │ ← 你在这里挂鱼饵
#                                ╰───╯
#   鱼饵 (你的代码)             🐛  ← 你写的逻辑
#
# 鱼线一直在正常走，但到了一个「钩子点」，系统停下来问：
# 「你有东西要挂上来吗？」
# 你挂了 → 执行你的逻辑 → 鱼线继续走
# 你没挂 → 鱼线直接继续走

print("=" * 65)
print("📌 钩子 = 系统给你留的「插手点」")
print("=" * 65)
print("""
  系统流程（没有钩子）：
    初始化 → 处理数据 → 输出结果
              │
              ↓  你无法插手

  系统流程（有钩子）：
    初始化 → [__len__钩子] → [__str__钩子] → [你自定义的钩子]
              │                 │                │
              ↓                 ↓                ↓
           你写len逻辑       你写显示逻辑      你写任何逻辑

  钩子 = 系统说：「到这步了，你想不想干点什么？」
""")


