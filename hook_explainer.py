# 可视化：一个对象被操作时，Python 在哪些地方留了钩子
print("""
  当你对对象做这个操作：          Python 检查这个钩子：
  ─────────────────────          ─────────────────
  len(obj)                       __len__()
  str(obj) / print(obj)          __str__()
  repr(obj)                      __repr__()
  obj + other                    __add__()
  obj[0] / obj[-1]               __getitem__()
  obj.key = value                __setattr__()
  del obj.key                    __delattr__()
  obj()  (像函数一样调用)         __call__()
  with obj:                      __enter__() / __exit__()
  for x in obj:                  __iter__() / __next__()
  bool(obj) / if obj:            __bool__()
""")

# 关键理解：
