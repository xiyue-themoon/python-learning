# ---------- 例子1：sort 的 key 参数 ----------
# sorted() 给你留了一个钩子：你想按什么规则排序？
words = ["apple", "Banana", "cherry", "Date"]

# 默认排序是按字母顺序（ASCII）
print(f"默认排序: {sorted(words)}")

# 但你想按长度排序 —— 用 key 钩子
print(f"按长度:   {sorted(words, key=len)}")

# 你想忽略大小写 —— 用 key 钩子
print(f"忽略大小写: {sorted(words, key=lambda w: w.lower())}")

# key= 就是 sorted() 给你的钩子！
# 它在内部做排序时，每遇到一个元素就问你的钩子：
# 「这个元素应该按什么值来排？」

print()
