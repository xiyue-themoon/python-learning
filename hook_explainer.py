# 假设 Python 没有 __str__ 这个钩子
class PersonNoHook:
    def __init__(self, name, age):
        self.name = name
        self.age = age

p = PersonNoHook("小明", 20)
print(f"print(p) → {p}")
print("  ↑ 打印出来的是 <__main__.PersonNoHook object at 0x...>")
print("  你看不懂这串东西，系统也不知道你想怎么显示")
print()


# ================================================================
# 3. 有钩子：你在系统流程里「插一脚」
# ================================================================
print("=" * 65)
print("3. 有钩子：系统说「你想怎么显示？你来写」")
print("=" * 65)

class PersonWithHook:
    def __init__(self, name, age):
        self.name = name
        self.age = age

    # ⬇⬇⬇ 这就是一个钩子 ⬇⬇⬇
    def __str__(self):       # ← 系统给 print() 留的钩子
        """print(p) 时，系统不自己决定怎么显示，
           而是调用这个方法，让你来告诉它怎么显示。"""
        return f"🙋 {self.name}（{self.age}岁）"

p2 = PersonWithHook("小明", 20)
print(f"print(p2) → {p2}")
print("  ↑ 系统调用了你挂在钩子上的逻辑，输出了你想要的格式")
print()

# ---------- 把「钩子」两个字换成更直觉的画面 ----------

print("--- 换个角度看 ---")
print("""
  没有钩子时：        系统             →  输出混乱的地址
                      你             →  干瞪眼

  有钩子时：          print(p) 被调用
                         ↓
                      Python 说：「等等，这个对象有 __str__ 吗？」
                         ↓
                      有！→ 调用你写的 __str__()
                         ↓
                      返回你想要的字符串 → 打印出来
""")


